TEST(Utility, Constructor)
{
    Utility utility (world_map1,20,20);

    ASSERT_EQ(20,utility.getMWidth());
    ASSERT_EQ(20, utility.getMHeight());
}

TEST(Utility, Setter)
{
    Utility utility (world_map1,20,20);

    int* map[500];

    utility.setMWidth(30);
    utility.setMHeight(17);
    utility.setMap(*map);

    ASSERT_EQ(30,utility.getMWidth());
    ASSERT_EQ(17,utility.getMHeight());
    ASSERT_EQ(*map,utility.getMap());
}

TEST(Utility, GetMap)
{
    Utility utility (world_map1,20,20);

    ASSERT_EQ(1,utility.GetMap(0,0));
    ASSERT_EQ(9,utility.GetMap(1,1));
}